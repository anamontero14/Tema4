package boletin3.ejer1;

public class Suma {

	/**
	 * Hace la suma de dos enteros
	 * 
	 * @param num1
	 * @param num2
	 * @return suma de dos enteros
	 */
	public int suma(int num1, int num2) {
		return num1 + num2;
	}

	/**
	 * Hace la suma de dos decimales
	 * 
	 * @param num1
	 * @param num2
	 * @return suma de dos decimales
	 */
	public double suma(double num1, double num2) {
		return num1 + num2;
	}

}
