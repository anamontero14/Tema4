package boletin3.ejer2;

public class Media {

	/**
	 * Método que hace la media
	 * 
	 * @param num1
	 * @param num2
	 * @return devuelve la media de dos numeros enteros
	 */
	public double media(int num1, int num2) {
		return (num1 + num2) / 2;
	}

	/**
	 * Método para hacer la media de 3 números
	 * 
	 * @param num1
	 * @param num2
	 * @param num3
	 * @return la media de 3 números
	 */
	public double media(int num1, int num2, int num3) {
		return (num1 + num2 + num3) / 3;
	}
}
