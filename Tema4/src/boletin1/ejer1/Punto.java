package boletin1.ejer1;

public class Punto {

	/** atributo para el punto horizontal */
	int x;

	/** atributo para el punto en vertical */
	int y;

	/**
	 * Constructor con dos atributos
	 * 
	 * @param x atributo para el punto horizontal
	 * @param y atributo para el punto vertical
	 */
	Punto(int x, int y) {
		this.x = x;
		this.y = y;
	}

}
